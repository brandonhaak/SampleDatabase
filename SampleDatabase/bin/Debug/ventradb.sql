IF OBJECT_ID('dbo.Transactions', 'U') IS NOT NULL 
  DROP TABLE dbo.Transactions; 
IF OBJECT_ID('dbo.Prices', 'U') IS NOT NULL 
  DROP TABLE dbo.Prices; 
CREATE TABLE Transactions (
	TID				INT IDENTITY(10001,1) PRIMARY KEY,
	TTIME			DATETIME NOT NULL,
	TTYPE			NVARCHAR(64) NOT  NULL,
	TOPERATOR		NVARCHAR(64) NOT  NULL,
	TLOCATION		NVARCHAR(64) NOT  NULL,
	TDESCRIPTION	NVARCHAR(64) NOT  NULL,
	TAMOUNT 		MONEY
);

CREATE TABLE Prices (
	TOPERATOR 	NVARCHAR(64) NOT NULL,
	Price 		MONEY NOT NULL
);